DROP TABLE IF EXISTS dbo.tbl_mstr_webuser;

CREATE TABLE dbo.tbl_mstr_webuser(
	   userid INT PRIMARY KEY
	 , username NVARCHAR(100)
	 , phone NVARCHAR(20)
	 , pin NVARCHAR(50)
     , email NVARCHAR(50)
	 , guid NVARCHAR(64)
	 , deviceid INT
	 , UpdateDate TIMESTAMP DEFAULT NOW());

