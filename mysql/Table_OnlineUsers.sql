DROP TABLE IF EXISTS dbo.tbl_usr_feedback;

CREATE TABLE dbo.tbl_usr_feedback(
	   id INT PRIMARY KEY AUTO_INCREMENT
	 , userid INT
	 , feedback NVARCHAR(500)
	 , FBDate TIMESTAMP DEFAULT NOW());

